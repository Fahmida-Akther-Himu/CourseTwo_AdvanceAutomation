package classOne;

public class PrimitiveDataType {
    public static void main(String[] args) {

        int integerValue = 2147483647;
        byte byteValue = 127;
        short shortValue = 32767;
        long longValue = 922335484;
        float floatValue = 689.89F;
        double doubleValue = 34848789.56487878;
        char charValue = 'a';
        boolean booleanValue = true;


        System.out.println("int : " + integerValue + "\nbyte : " + byteValue + "\nshort : " + shortValue + "\nlong : " + longValue + "\nfloat : " + floatValue +
                "\ndouble : " + doubleValue + "\nchar : " + charValue + "\nboolean : " + booleanValue);


    }
}
