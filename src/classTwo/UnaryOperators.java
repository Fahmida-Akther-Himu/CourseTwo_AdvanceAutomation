package classTwo;

public class UnaryOperators {
    public static void main(String[] args) {

        int a = 10;
        a++;
        System.out.println(a);
        int d = ++a;
        System.out.println(d);
        a--;
        System.out.println(a);
        int e = --a;
        System.out.println(e);

    }
}
