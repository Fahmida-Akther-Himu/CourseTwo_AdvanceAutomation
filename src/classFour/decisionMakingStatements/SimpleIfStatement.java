package classFour.decisionMakingStatements;

public class SimpleIfStatement {
    public static void main(String[] args) {

        int number = 11;

        if (number % 2 == 0) {
            System.out.println("This is even number");
        }

        if (number % 2 != 0) {
            System.out.println("This is odd number");
        }
    }
}
